#include <stdio.h>

int main() {
    // Nomes de variáveis na convenção ANSI C.
    int numero = 10;      // Variável declarada com valor inicial
    double valor_produto; // Variável declarada sem valor inicial

    // Nomes de variáveis no padrão Camel-Case, usado em C++.
    int diaDaSemana;
    double valorProduto; 

    // Má pratica de programação
    int a, x1;
    double preço, VALOR;

    // Nomes de variáveis inválidos
    // int 1variavel, @variavel;
    
    // É possível alterar o valor de uma variável.
    numero = 20; 

    return 0;
}
